#what is realtion operations
# Relation operations are used to compare two values or expressions.
# They return a boolean value (True or False) based on the comparison.
# Common relation operations include:
# - Equal to (==)
# - Not equal to (!=)
# - Greater than (>)
# - Less than (<)
# - Greater than or equal to (>=)
# - Less than or equal to (<=)

# Example of relation operations in Python
a = 10
b = 20
# Equal to
if a == b:
    print("a is equal to b")
else:
    print("a is not equal to b")
# Not equal to
if a != b:
    print("a is not equal to b")
else:
    print("a is equal to b")
# Greater than
if a > b:
    print("a is greater than b")
else:
    print("a is not greater than b")
# Less than
if a < b:
    print("a is less than b")
else:
    print("a is not less than b")
# Greater than or equal to
if a >= b:
    print("a is greater than or equal to b")


else:
    print("a is less than b")
# Less than or equal to
if a <= b:
    print("a is less than or equal to b")
else:
    print("a is greater than b")
# Example of relation operations with variables
x = 5
y = 10
# Check if x is less than y
