# List in Python
# A list is a collection of items that are ordered and changeable.
# Types of Lists in Python
# 1. Homogeneous List: A list that contains elements of the same data type.
# 2. Heterogeneous List: A list that contains elements of different data types. 
# 3. Nested List: A list that contains other lists as its elements. 
# 4. Multidimensional List: A list that contains lists within lists, forming a matrix-like structure.
# 5. Empty List: A list that contains no elements.
# 6. List of Lists: A list that contains multiple lists as its elements.
# 7. List of Tuples: A list that contains tuples as its elements.
# 8. List of Dictionaries: A list that contains dictionaries as its elements.
# 9. List of Sets: A list that contains sets as its elements.
# 10. List of Strings: A list that contains strings as its elements.
# Example of different types of lists
homogeneous_list = [1, 2, 3, 4, 5]  # All elements are integers
heterogeneous_list = [1, "two", 3.0, True]  # Mixed data types
nested_list = [[1, 2], [3, 4], [5, 6]]  # List containing lists
multidimensional_list = [[[1, 2], [3, 4]], [[5, 6], [7, 8]]]  # List of lists forming a matrix
empty_list = []  # No elements in the list
list_of_lists = [[1, 2], [3, 4], [5, 6]]  # List containing multiple lists
list_of_tuples = [(1, 2), (3, 4), (5, 6)]  # List containing tuples
list_of_dicts = [{"name": "Alice"}, {"name": "Bob"}]  # List containing dictionaries
list_of_sets = [{1, 2}, {3, 4}, {5, 6}]  # List containing sets
list_of_strings = ["apple", "banana", "cherry"]  # List containing strings  
# Printing the lists
print("Homogeneous List:", homogeneous_list)
print("Heterogeneous List:", heterogeneous_list)
print("Nested List:", nested_list)
print("Multidimensional List:", multidimensional_list)
print("Empty List:", empty_list)



